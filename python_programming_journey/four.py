# 1 - Numeric Data Type
a = 5  # Integer
b = 5.4 # Float
c = complex(5,6) # Complex Number
print(type(a))
print(type(b))
print(type(c))

# 2 - Sequence Data Type
name = "Aditya" # String
value = [5,6,7,8,9,'Manish'] # List
value1 = (5,6,7,8,9,'Sarita') # Tuple
print(type(name))
print(type(value))
print(type(value1))

# 3 - Dictonary Data Type
my_dict = {'name': 'Aditya', 'Age': 26, 'city': 'Gorakhpur'}
print(type(my_dict))

# 4 - Sets
my_sets = {5,6,7,8,9,'Sarita'}
print(type(my_sets))

# 5 - Boolean
bool1 = True
bool2 = False
print(type(bool1))
print(type(bool2))

# 6 - Binary
byte1 = b'Aditya'
byte2 = b"Manisha"
print(type(byte1))
print(type(byte2))